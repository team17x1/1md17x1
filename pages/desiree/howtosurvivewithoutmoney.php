                            Just like you would search for a job in your home country, you apply for job openings in the country you’re in now. Many places get busier during holidays for example, so you could have a good chance of landing a job even if you’re only planning on staying a few months.</p>

                        <p>

                            (Of course, technically to be able to do this, you must have a work permission and all the right kind of paperwork… So of course, I have never done this myself, just for the record :) )


                        </p>
                    </div>
                </div>



                <div class="container">

                    <div class="title2"><span><img src="images/arrow.svg" alt="Expand text" class="expandimg"> Do a workaway!</span>

                    </div>
                    <div class="content2">
                        <p>


                            Workaway.org is a worldwide platform to connect with other people – it is for hosts, who are searching for volunteer workers, and volunteers who are looking for a place to work.
                        </p>
                        <p>
                            So basically, it is a platform for job openings in all parts of the world. But the perks of this is, that you get around all the hassle of paperwork, since you will not get paid. Instead the host provides accommodation for you, and sometimes food expenses are covered as well.


                        </p>
                    </div>
                </div>


                <div class="container">

