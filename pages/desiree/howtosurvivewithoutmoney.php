                    <div class="content3">
                        <p>

                            Staying at someone’s house for free + having a local tour guide to show you all the cool things to do + a potential new friend. What have you got to lose?

                        </p>
                    </div>
                </div>

                <h2 class="margintop100">Minimize your expenses</h2>


                <div class="container">

                    <div class="title4"><span><img src="images/arrow.svg" alt="Expand text" class="expandimg">  Stay at hostels</span>

                    </div>
                    <div class="content4">
                        <p>

                            Often you can get a cleaning job or reception hours to get free or at least a cheaper rent. If not – you still save a bunch compared to staying at a hotel. Also, you’re bound to meet a lot of interesting people in a hostel.

                        </p>
                    </div>
                </div>



                <div class="container">

                    <div class="title5"><span><img src="images/arrow.svg" alt="Expand text" class="expandimg">  Get to know the locals</span>

                    </div>
                    <div class="content5">
                        <p>
                            It is super helpful to have local friends who’re already established when coming to a new city. Something that would make a huge difference for you as a newcomer, might be the smallest insignificant thing to do for someone else.
                        </p>
                        <p>
                            Let’s say maybe you have to pay a whole lot for a metro pass to get to work in your new city… But hey – you discover that your new neighbor works in the same area as you, and he’s got a car. So just go ask the guy if you could do carpooling in the morning. Who knows, maybe he would benefit from it too.
                        </p>
                        <p>
                            Of course you shouldn’t exploit people, but take the time to get to know the people around you. For all that I know, I bet you also have some qualities or skills that could be useful for them.
                        </p>
                    </div>
                </div>



                <div class="container">

                    <div class="title6"><span><img src="images/arrow.svg" alt="Expand text" class="expandimg">  Go with the flow</span>

                    </div>
                    <div class="content6">
                        <p>
                            Go for the low-fare tickets, when picking your next destination. If you’re a ‘go-with-the-flow’ kind of person, and you haven’t got a set plan for when to go where, you can get low-fare tickets for practically nothing. It can be a surprisingly cheap option. And you might end up going to a destination you would never have thought of, if you hadn’t been browsing for the cheapest tickets avaliable to anywhere.
                        </p>
                        <p>
                            (A few months ago, I found a bus-ticket from Milan to Switzerland for as little as 3€. I hadn’t planned on going to the Swiss side of the Alps, but I figured, for that price – why not, right?)
                        </p>
                    </div>
                </div>





                <h2 class="margintop100">How to save on transportation</h2>


                <div class="container">

                    <div class="title7"><span><img src="images/arrow.svg" alt="Expand text" class="expandimg">  Hitch hike between your destinations</span>

                    </div>
                    <div class="content7">
                        <p>

                            This is by far the cheapest way of getting around. It can be very time consuming though, and you just can’t know when you’ll arrive. So make sure you have time enough if you’re going with this option. But the effort is worth it – you’ll get from A to B without spending anything + you might meet some really cool people on the way.

                        </p>
                    </div>
                </div>



                <div class="container">

