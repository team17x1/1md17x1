                    <h2>Go abroad for a semester</h2>
                    <p>Are you a student at a higher level educational program? And do you have dreams of leaving your home country for a little while, and explore the world a bit? Luckily, those two things don’t necessarily have to exclude each other.</p>

                    <p>Many educational institutions have arrangements with partner schools all over the world. So, the students have the opportunity to apply for a semester abroad at one of these schools.</p>
                </section>


                <section>
                    <h2>Pros of going abroad</h2>

                    <ul>
                        <li>Every country has its own teaching culture. When going on exchange you’ll learn to be taught in a different style than what you’re used to. </li>

                        <li>You’ll get a new perspective of your field.</li>

                        <li>You’ll learn to be independent, and you’ll learn how to adapt.</li>

                        <li>You’ll meet a lot of new friends from all over the world, and some of them will later on when you graduate, become a part of your professional network. </li>

                    </ul>
                </section>

                <section>
                    <h2>Choosing which school(s) to apply to</h2>
                    <p>Different schools have different specializations &mdash; often you can download an entire list of the partner institutions from your school's website. And then you just have to explore the different schools’ course catalogues, to discover which school(s) would be the most relevant and interesting for you to apply to.</p>

                </section>
                <section>
                    <h2>Writing your application</h2>
                    <p>So the exchange program is a great opportunity to see a different part of the world, and a chance to try studying in a different learning environment. However, there might be a limited number of exchange spots avaliable at the school you want to go to. So it is important to do some real consideration when it comes to writing your application letter. Typically, you’ll have to fill out a bunch of forms AND write a motivated letter. And the motivated application letter in particular, is your chance to influence whether it will be YOU, to be elected for the exchange.</p>

                    <p><strong>Don’t:</strong></p>
                   <ul> 
                    <li>A bad example of a letter like this could be something like: “I applied to School of XXX in XXX-city, because I really want to study abroad, and I would really like to go on exchange at this school.”</li>
</ul>
                    <p><strong>Do's:</strong></p>

                    <ul>
                        <li>
                            It could be a good idea to describe what exactly made you choose that school over the others - e.g. field of specializations offered by the school, the teaching methods, etc. </li>

                        <li>What will you be able to contribute with? What are your strengths – are you really good at being structured and disciplined regarding your studies, or are you very creative and innovative?</li>


                        <li>Remember, while being abroad, you’ll be a representative for your home institution. Because of this, it could also be a good idea to describe why you’d be a good ambassador to represent your school. This could be something like your way of being a professional and structured student, your passion for your field and your personal drive.</li>
                    </ul>
                </section>

